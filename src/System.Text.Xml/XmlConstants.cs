namespace System.Text.Xml;

/// <summary>
/// Provides UTF-8 byte constants used by XML readers and writers.
/// </summary>
public static class XmlConstants
{
    /// <summary>Represents the '&lt;' character.</summary>
    public const byte LessThan = (byte)'<';

    /// <summary>Represents the '&gt;' character.</summary>
    public const byte GreaterThan = (byte)'>';

    /// <summary>Represents the '/' character.</summary>
    public const byte Slash = (byte)'/';

    /// <summary>Represents the '=' character.</summary>
    public const byte EqualSign = (byte)'=';

    /// <summary>Represents the ':' character.</summary>
    public const byte Colon = (byte)':';

    /// <summary>Represents the ';' character.</summary>
    public const byte Semicolon = (byte)';';

    /// <summary>Represents the '"' character.</summary>
    public const byte Quote = (byte)'"';

    /// <summary>Represents the '\'' character.</summary>
    public const byte Apostrophe = (byte)'\'';

    /// <summary>Represents the '&amp;' character.</summary>
    public const byte Ampersand = (byte)'&';

    /// <summary>Represents the '!' character.</summary>
    public const byte Exclamation = (byte)'!';

    /// <summary>Represents the '?' character.</summary>
    public const byte Question = (byte)'?';

    /// <summary>Represents the '-' character.</summary>
    public const byte Hyphen = (byte)'-';

    /// <summary>Represents the '[' character.</summary>
    public const byte OpenBracket = (byte)'[';

    /// <summary>Represents the ']' character.</summary>
    public const byte CloseBracket = (byte)']';

    /// <summary>Represents the space character.</summary>
    public const byte Space = (byte)' ';

    /// <summary>Represents the horizontal tab character.</summary>
    public const byte Tab = (byte)'\t';

    /// <summary>Represents the line feed character.</summary>
    public const byte LineFeed = (byte)'\n';

    /// <summary>Represents the carriage return character.</summary>
    public const byte CarriageReturn = (byte)'\r';
}